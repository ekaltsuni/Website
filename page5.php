    <h2>Biography</h2>
    <div class="content">
        <p>&ensp;<em>Banksy</em> is a pseudonymous England-based <strong>street artist</strong>, <strong>
                activist</strong>, and <strong>film director</strong>, active since the 1990s. His satirical street art
            and subversive epigrams combine dark humour with graffiti executed in a distinctive stenciling technique.
            His works of political and social commentary have appeared on <ins>streets</ins>, <ins>walls</ins>, and
            <ins>bridges</ins> throughout the world.
        </p>
        <p>&ensp;Banksy's work grew out of the Bristol underground scene, which involved collaborations between artists
            and musicians. Banksy says that he was inspired by 3D, a graffiti artist and founding member of the musical
            group Massive Attack.</p>
        <p>&ensp;Banksy displays his art on publicly visible surfaces such as walls and self-built physical prop pieces.
            Banksy no longer sells photographs or reproductions of his street graffiti, but his public "installations"
            are regularly resold<del>, often even by removing the wall they were painted on</del>.</p>
