        <h2>Best Sreet Art Cities in Europe</h2>
        <h3 id="England">1. London and Bristol, England</h3>
        <table class="table-class">
            <tr class="cell-class">
                <td>
                    <img src="images/england.jpg" alt="This delicate heron by Roa is just off Brick Lane." />
                </td>
            </tr>
            <tr>
                <td class="image-label">
                    This delicate heron by Roa is just off Brick Lane.
                </td>
            </tr>
        </table>
        <p>
            London’s street art is defined by an ethic of guerrilla installations,
            layered messes and constant do-overs. You can find a great concentration
            of perpetually evolving works in the Shoreditch neighborhood.
            <br />
            <br />
            Bristol was one of the first to really own its identity as a street art
            city. They grabbed the distinction with both hands and now have a
            thriving community with commissioned and guerrilla works covering three
            neighborhoods and fueled by the huge annual Upfest street art festival.
        </p>
        <h3 id="Belfast">2. Belfast, Northern Ireland</h3>
        <table class="table-class">
            <tr class="cell-class">
                <td>
                    <img src="images/belfast.jpg" alt="One of Connor Harrington’s set pieces in downtown Belfast." />
                </td>
            </tr>
            <tr>
                <td class="image-label">
                    One of Connor Harrington’s set pieces in downtown Belfast.
                </td>
            </tr>
        </table>
        <p>
            Belfast’s street art looks backward and forward. You can tour the
            sectarian murals for a lesson on the polarizing effects of civil war and
            then wander downtown for equally edgy but more modern works from artists
            like Connor Harrington.
        </p>
        <h3 id="Paris">3. Paris, France</h3>
        <table class="table-class">
            <tr class="cell-class">
                <td>
                    <img src="images/paris.jpg" alt="D-Face in the 13th arr." />
                </td>
            </tr>
            <tr>
                <td class="image-label">D-Face in the 13th arr.</td>
            </tr>
        </table>
        <p>
            Paris is a mix between London and Chicago. Like Chicago, they have a big
            walls project in the 13th arrondissement which features huge pieces from
            the likes of Shepard Fairy and D-Face. Like London, the Belleville
            neighborhood and canal areas house an ever-changing variety of smaller
            murals, graffiti and multi-media materials.
        </p>
        <h3 id="Berlin">4. Berlin, Germany</h3>
        <table class="table-class">
            <tr class="cell-class">
                <td>
                    <img src="images/berlin.jpg" alt="Urban Nation’s street art museum in Berlin.
            " />
                </td>
            </tr>
            <tr>
                <td class="image-label">
                    Urban Nation’s street art museum in Berlin.
                </td>
            </tr>
        </table>
        <p>
            Graffiti may be illegal in Berlin but that doesn’t prevent its artists
            from creating a whole lot of it. Berlin’s own particular brand of street
            art was influenced heavily by a historically artsy culture and the cheap
            rents and underutilized buildings that surfaced after the wall came
            down. It’s chaotic, political and and clever. Not only do they have some
            of the best graffiti in the world, but they have some large scale murals
            and an urban art museum.
        </p>
        <h3 id="Stavenger">5. Stavenger, Norway</h3>
        <table class="table-class">
            <tr class="cell-class">
                <td>
                    <img src="images/stavenger.jpg" alt="Silo mural by Fintan Magee." />
                </td>
            </tr>
            <tr>
                <td class="image-label">Silo mural by Fintan Magee.</td>
            </tr>
        </table>
        <p>
            This medium sized city nestled among Norway’s fjords and hiking paths
            may not be an obvious place for street art to flourish, but Stavenger
            has made it happen. Their mural culture is largely curated by the Nuart
            festival, which occurs in September every year. They are deliberately
            challenging conventional notions of art, all over Stavenger.
        </p>
        <h3 id="Reykjavik">6. Reykjavik, Iceland</h3>
        <table class="table-class">
            <tr class="cell-class">
                <td>
                    <img src="images/reykjavik.jpg" alt="Harbor district." />
                </td>
            </tr>
            <tr>
                <td class="image-label">Harbor district.</td>
            </tr>
        </table>
        <p>
            Street art in Reykjavik is heating up. They don’t have a huge culture
            built around it, but several collaboration events with Urban Nation have
            populated the city with some world class murals.
        </p>
        <h3 id="Estonia">7. Tallinn and Tartu, Estonia</h3>
        <table class="table-class">
            <tr class="cell-class">
                <td>
                    <img src="images/estonia.jpg" alt="This evocative wall tells the story of the legend of Loit & Hamarik, lovers who never touched.
            " />
                </td>
            </tr>
            <tr>
                <td class="image-label">
                    This evocative wall tells the story of the legend of Loit & Hamarik,
                    lovers who never touched.
                </td>
            </tr>
        </table>
        <p>
            Estonia may be the picture perfect poster child for UNESCO heritage, but
            they also have some serious street art going on. Their particular brand
            of it comes with a strong dose of folk culture, nature and wry
            commentary on their political climate.
        </p>
