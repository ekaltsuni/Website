        <h2>Popular Sreet Artists</h2>
        <h3 id="banksy">1. <a href="page5.php">Banksy</a></h3>
        <table class="table-class">
            <tr class="cell-class">
                <td>
                    <img src="images/banksy-balloon-girl.jpg" alt="Banksy, Ballon Girl" />
                </td>
            </tr>
            <tr>
                <td class="image-label">Bansky, Ballon Girl (London)</td>
            </tr>
        </table>
        <p>
            <a href="page5.php">Banksy's</a> work brings many social and political
            issues to light. As a result, many of his pieces have been the target of
            vandals. Some have been removed for unrelated reasons. Those that remain
            are highly sought after. After years of speculation, the true identity
            of this street art superstar is still unknown. From Robert Del Naja
            (a.k.a. 3D) of Massive Attack to Jamie Hewlett, British comic book
            artist and designer—theories abound! One thing that isn’t up for debate
            is that his influence on street art has been remarkable! He’s even been
            nominated for an Oscar for the 2010 film “Exit Through the Gift Shop.”
            (In true <a href="page5.php">Banksy</a> fashion, he requested to attend
            the award ceremony incognito. His request was denied.)
        </p>
        <h3>2. Roa</h3>
        <table class="table-class">
            <tr class="cell-class">
                <td>
                    <img src="images/roa-caged-animals.jpeg" alt="Roa, Caged Animals" />
                </td>
            </tr>
            <tr>
                <td class="image-label">
                    Roa, North West Walls Caged Animals (Belgium)
                </td>
            </tr>
        </table>
        <p>
            Roa has mastered his craft in cities across the continents of Europe,
            North America, Australia, and Africa, making him one of the most widely
            famous street artists on our list. His style is recognizable as vibrant
            black and white paintings of birds and animals that are native to the
            area in which he paints. He is very private and lives his personal life
            is under a veil of secrecy. His paintings are very intimate, considering
            the fact that they are large and out in the open for all to see. Roa’s
            topics are often described as a combination of life, death, and life
            after death.
        </p>
        <h3>3. Lady Pink</h3>
        <table class="table-class">
            <tr class="cell-class">
                <td>
                    <img src="images/lady-pink-2.jpeg" alt="Lady Pink, NY" />
                </td>
            </tr>
            <tr>
                <td class="image-label">Lady Pink (New York)</td>
            </tr>
        </table>
        <p>
            Lady Pink is an Ecuadorian-American feminist graffiti artist based in
            NYC. Her work features messages that empower women. The murals are
            colorful and feature famous people, animals, social motives, and more.
            This street artist was nicknamed “the first lady of graffiti” because
            she was the first active female artist in the early 80s. Later she had
            her own all-female crew and was featured in many publications and
            galleries like the Whitney Museum, The Metropolitan Museum of New York
            City, the Brooklyn Museum, and more.
        </p>
        <h3>4. Vhils</h3>
        <table class="table-class">
            <tr class="cell-class">
                <td>
                    <img src="images/vhils.jpg" alt="Vhils, mural in tribute to health workers" />
                </td>
            </tr>
            <tr>
                <td class="image-label">Vhils, (São João Hospital, Lisbon)</td>
            </tr>
        </table>
        <p>
            Alexandre Manuel Dias Farto, tag name Vhils, is a famous Portuguese
            street artist. His specific style of faces carved into walls gained
            popularity when it was part of the Cans Festival in London in 2008
            alongside <a href="page5.php">Banksy</a>. He is based in Lisbon and
            London but has worked all over the world, from Hong Kong to Spain. His
            work and unique approach to art has been shown in solo and group
            exhibitions in over 30 countries. Some of the galleries that featured
            his work were Centre Pompidou (Paris), Barbican Centre (London), the
            Museum of Contemporary Art San Diego (San Diego), and many more.
        </p>
        <h3>5. OSGEMEOS</h3>
        <table class="table-class">
            <tr class="cell-class">
                <td>
                    <img src="images/osgemeos.jpeg" alt="The Giant of Boston" />
                </td>
            </tr>
            <tr>
                <td class="image-label">
                    OSGEMEOS, The Giant of Boston (Boston, USA)
                </td>
            </tr>
        </table>
        <p>
            “Os gemeos” is Portuguese for “the twins.” Otavio Pandolfo and Gustavo
            Pandolfo are identical twins famous, across the world, for their
            paintings and murals. Their dream-like style was described as “escapist
            fantasies.” It includes patterns, cartoon-looking characters, and
            illustrations. This duo’s characters are often painted with yellow skin,
            and their murals and tags comment on political and social life in
            Brazil. Their murals have a lot of folklore and cultural subject matter
            showing their Brazilian heritage.
        </p>
