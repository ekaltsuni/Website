        <h1>The World of Street Art</h1>
        <img src="images/header-street-art.jpeg" alt="header-image-srt-wall" />
    </div>
    <div class="content">
        <p>
            Street art is
            <a href="https://en.wikipedia.org/wiki/Visual_art" target="_blank">
                visual art
            </a>
            created in public locations for public visibility. It has been
            associated with the terms "independent art," "post-graffiti",
            "neo-graffiti" and
            <a href="https://en.wikipedia.org/wiki/Guerrilla_art" target="_blank">guerrilla art</a>. Street art has
            evolved from the early forms of defiant
            <a href="https://en.wikipedia.org/wiki/Graffiti" target="_blank">graffiti</a>
            into a more commercial form of art, as one of the main differences now
            lies with the messaging. Street art is often meant to provoke thought
            rather than rejection among the general audience through making its
            purpose more evident than that of
            <a href="https://en.wikipedia.org/wiki/Graffiti" target="_blank">graffiti</a>. The issue of permission has
            also come at the heart of street art, as
            <a href="https://en.wikipedia.org/wiki/Graffiti" target="_blank">graffiti</a>
            is usually done illegally, whereas street art can nowadays be the
            product of an agreement or even sometimes a commission. However, it
            remains different from traditional art exposed in public spaces by its
            explicit use of said space in the conception phase.
        </p>
    </div>
    <h2>Background</h2>
    <div class="content">
        <p>
            Street art is a form of artwork that is displayed in public on
            surrounding buildings, on streets, trains and other publicly viewed
            surfaces. Many instances come in the form of guerrilla art, which is
            intended to make a personal statement about the society that the
            <a href="page2.php">artist</a>
            lives within. The work has moved from the beginnings of
            <a href="https://en.wikipedia.org/wiki/Graffiti" target="_blank">graffiti</a>
            and vandalism to new modes where <a href="page2.php">artists</a> work
            to bring messages, or just beauty, to an audience. Some
            <a href="page2.php">artists</a> may use "smart vandalism" as a way to
            raise awareness of social and political issues, whereas other
            <a href="page2.php">artists</a>
            use urban space as an opportunity to display personal artwork.
            <a href="page2.php">Artists</a>
            may also appreciate the challenges and risks that are associated with
            installing illicit artwork in public places. A common motive is that
            creating art in a format that utilizes public space allows
            <a href="page2.php">artists</a> who may otherwise feel disenfranchised
            to reach a much broader audience than other styles or galleries would
            allow. Whereas traditional
            <a href="https://en.wikipedia.org/wiki/Graffiti" target="_blank">graffiti</a>
            <a href="page2.php">artists</a> have primarily used spray paint to
            produce their work, "street art" can encompass other media, such as LED
            art, mosaic tiling, stencil art, sticker art, reverse
            <a href="https://en.wikipedia.org/wiki/Graffiti" target="_blank">graffiti</a>, "Lock On" sculptures,
            wheatpasting, woodblocking, yarn bombing and
            rock balancing. New media forms such as video projections onto large
            city buildings are an increasingly popular tool for street
            <a href="page2.php">artists</a>â€”and the availability of cheap hardware
            and software allows such artwork to become competitive with corporate
            advertisements. <a href="page2.php">Artists</a> are thus able to create
            art from their personal computers for free, which competes with
            companies' profits.
        </p>
